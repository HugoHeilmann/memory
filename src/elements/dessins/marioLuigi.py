from usefull.usefull import *


def mario_luigi(t, type_color, pixel_size):
    t.speed(100)

    if type_color == 1:
        clothes_color = "#FF0000"
    else:
        clothes_color = "#32CD32"

    # Chaussures
    t.color("#663300")
    row_right_squares(t, 4, pixel_size)
    t.backward(3 * pixel_size)
    row_left_squares(t, 3, pixel_size)
    t.up()
    t.forward(4 * pixel_size)
    t.down()
    row_left_squares(t, 3, pixel_size)
    t.backward(3 * pixel_size)
    row_right_squares(t, 4, pixel_size)

    # Salopette
    t.up()
    t.left(90)
    t.forward(2 * pixel_size)
    t.left(90)
    t.forward(2 * pixel_size)
    t.color("#000077")
    t.down()
    row_left_squares(t, 3, pixel_size)
    t.forward(2 * pixel_size)
    row_left_squares(t, 3, pixel_size)
    t.left(180)
    row_left_squares(t, 8, pixel_size)
    t.left(90)
    low_traverse(t, pixel_size)
    row_right_squares(t, 6, pixel_size)
    t.right(90)
    t.forward(pixel_size)
    t.right(90)
    row_left_squares(t, 1, pixel_size)
    t.forward(pixel_size)
    row_left_squares(t, 2, pixel_size)
    t.forward(pixel_size)
    left_square(t, pixel_size)
    t.left(90)
    t.forward(pixel_size)
    t.left(90)
    row_right_squares(t, 4, pixel_size)
    t.right(90)
    row_right_squares(t, 3, pixel_size)
    t.backward(2 * pixel_size)
    t.right(90)
    t.forward(3 * pixel_size)
    left_square(t, pixel_size)

    # Boutons
    t.up()
    t.right(90)
    t.forward(pixel_size)
    t.color("#FFFF00")
    t.down()
    left_square(t, pixel_size)
    t.up()
    t.right(90)
    t.forward(2 * pixel_size)
    t.down()
    left_square(t, pixel_size)

    # Mains
    t.up()
    t.forward(3 * pixel_size)
    t.color("#FFA07A")
    t.down()
    row_left_squares(t, 2, pixel_size)
    t.left(90)
    t.forward(2 * pixel_size)
    t.left(90)
    row_right_squares(t, 2, pixel_size)
    t.backward(2 * pixel_size)
    row_left_squares(t, 3, pixel_size)
    t.up()
    t.forward(6 * pixel_size)
    t.down()
    row_left_squares(t, 3, pixel_size)
    t.backward(2 * pixel_size)
    row_right_squares(t, 2, pixel_size)
    t.left(90)
    t.forward(2 * pixel_size)
    t.left(90)
    row_left_squares(t, 2, pixel_size)

    # T-shirt
    t.color(clothes_color)
    left_square(t, pixel_size)
    t.backward(2 * pixel_size)
    walk_along_up_square_right(t, pixel_size)
    row_left_squares(t, 4, pixel_size)
    t.backward(3 * pixel_size)
    row_right_squares(t, 3, pixel_size)
    t.up()
    t.forward(pixel_size)
    t.down()
    row_right_squares(t, 2, pixel_size)
    t.up()
    low_traverse(t, pixel_size)
    t.right(90)
    t.down()
    row_right_squares(t, 1, pixel_size)
    left_square(t, pixel_size)
    row_right_squares(t, 3, pixel_size)
    t.backward(4 * pixel_size)
    t.right(90)
    t.forward(2 * pixel_size)
    t.left(90)
    row_left_squares(t, 3, pixel_size)
    t.backward(3 * pixel_size)
    row_right_squares(t, 2, pixel_size)
    t.left(180)
    t.up()
    t.forward(3 * pixel_size)
    t.down()
    row_left_squares(t, 3, pixel_size)

    # Visage
    t.up()
    t.forward(2 * pixel_size)
    t.left(90)
    t.forward(2 * pixel_size)
    t.left(90)
    t.color("#FFA07A")
    t.down()
    row_left_squares(t, 7, pixel_size)
    t.right(90)
    t.forward(pixel_size)
    row_left_squares(t, 2, pixel_size)
    t.backward(2 * pixel_size)
    t.right(90)
    row_right_squares(t, 4, pixel_size)
    t.backward(2 * pixel_size)
    row_left_squares(t, 3, pixel_size)
    t.up()
    t.forward(pixel_size)
    t.down()
    row_left_squares(t, 3, pixel_size)
    t.up()
    t.left(90)
    long_left_diagonal_from_above(t, pixel_size)
    t.down()
    row_left_squares(t, 2, pixel_size)
    left_square(t, pixel_size)
    right_square(t, pixel_size)
    t.forward(pixel_size)
    ### Yeux ###
    t.color("#000000")
    left_square(t, pixel_size)
    right_square(t, pixel_size)
    t.forward(pixel_size)
    ### End ###
    t.color("#FFA07A")
    row_left_squares(t, 3, pixel_size)
    t.backward(3 * pixel_size)
    row_right_squares(t, 2, pixel_size)

    # Pilosit√©
    t.color("#663300")
    row_right_squares(t, 3, pixel_size)
    t.left(90)
    row_right_squares(t, 3, pixel_size)
    t.left(90)
    row_left_squares(t, 1, pixel_size)
    t.up()
    t.forward(4 * pixel_size)
    t.down()
    row_left_squares(t, 4, pixel_size)
    t.backward(3 * pixel_size)
    t.left(90)
    row_right_squares(t, 2, pixel_size)
    t.up()
    t.left(90)
    t.forward(3 * pixel_size)
    t.down()
    row_left_squares(t, 2, pixel_size)
    t.right(90)
    row_right_squares(t, 2, pixel_size)

    # Casquette
    t.color(clothes_color)
    row_left_squares(t, 1, pixel_size)
    t.right(90)
    row_left_squares(t, 5, pixel_size)
    t.backward(5 * pixel_size)
    row_right_squares(t, 8, pixel_size)
