    row_left_squares(t, 1, pixel_size)
    row_right_squares(t, 1, pixel_size)
    t.right(90)
    t.forward(2 * pixel_size)
    t.left(90)
    row_left_squares(t, 1, pixel_size)
    row_right_squares(t, 2, pixel_size)
    row_left_squares(t, 1, pixel_size)
    double_walk_along_up_square_left(t, pixel_size)
    row_right_squares(t, 1, pixel_size)
    row_left_squares(t, 1, pixel_size)
    row_right_squares(t, 1, pixel_size)
